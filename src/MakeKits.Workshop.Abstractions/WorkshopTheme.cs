namespace MakeKits.Workshop;

/// <summary>
/// Visual theme applied to a workshop view.
/// </summary>
public enum WorkshopTheme
{
    /// <summary>
    /// No theme preference; the host chooses the default appearance.
    /// </summary>
    None,

    /// <summary>
    /// A dark visual theme.
    /// </summary>
    Dark,

    /// <summary>
    /// A light visual theme.
    /// </summary>
    Light,
}
